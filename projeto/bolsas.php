      <h5 class="text-left">
      Programa Institucional de Bolsas de Extensão da Universidade Estadual de Santa Cruz - PROBEX/UESC. Tem como objetivo Incentivar a participação de alunos de graduação nas atividades de extensão da UESC, contribuindo para o seu fortalecimento institucional. Contribuindo para que o docente possa transmitir aos alunos, através das ações extensionistas por ele coordenadas, os conhecimentos e experiências adquiridos ao longo de sua vida acadêmica, associando teoria e prática. Além de contribuir também para o fortalecimento das relações Universidade e sociedade.</h5><br>
      <h5 class="text-left ">Conforme Resolução CONSEPE Nº 49/2017 quanto à inscrição no Programa</h5><br>

      <h5 class="text-left">As ações extensionistas cadastradas na Pró-Reitoria de Extensão que estiverem em situação regular poderão ser inscritas pelos respectivos coordenadores, em Editais específicos, divulgadas pela Pró-Reitoria, para concorrerem às bolsas do PROBEX.</h5><br>


      <h5 class="text-left">Acesse o link da <a href="https://www2.uesc.br/publicacoes/resolucoes/arquivo/2023/03/01_2023.pdf" target="_blank">Resolução CONSU Nº 01/2023</a> que define o valor das Bolsas.</h5><br><br>

      <h3 class="text-center text-primary">São objetivos do PROBEX/UESC:</h3><hr><br>

      <h5 class="text-left"><b class= "darken-2">I - </b> Incentivar a participação de alunos de graduação nas atividades de extensão da UESC, contribuindo para o seu fortalecimento institucional e desenvolvimento regional.</h5><br>

      <h5 class="text-left"><b class= "darken-2">II - </b> Contribuir para que docentes e discentes compartilhem conhecimentos e experiências, por meio das práticas extensionistas, consolidando a relação entre teoria e prática.</h5><br>

      <h5 class="text-left"><b class="darken-2">III - </b> Contribuir para o fortalecimento das relações Universidade e sociedade.</h5><br>

